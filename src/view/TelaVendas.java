    private void txtCodProdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtCodProdActionPerformed
        // TODO add your handling code here:
        DefaultTableModel venda;

        ProdutoDAO dao = new ProdutoDAO();

        Produto p = new Produto();
        venda = (DefaultTableModel) tbProdVendas.getModel();

        if (!txtCodProd.getText().equals("")) {
            p.setCodigo(Integer.valueOf(txtCodProd.getText()));

            dao.buscarProduto(p);

            //item = 1 + venda.getRowCount();
            codProd = p.getCodigo();
            descProd = p.getDescricao();
            unProd = p.getUnidade();

            if (!txtQuantidade.getText().equals("")) {
                qtdProd = Integer.parseInt(txtQuantidade.getText());
            } else {
                qtdProd = 1;
            }

            valorUn = p.getPrecoVenda();
            valorTotal = valorUn * qtdProd;
            //totalVenda += valorTotal;

            if (p.getDescricao() == null) {

                JOptionPane.showMessageDialog(null, "Não encontrado!");
                txtCodProd.setText(null);
                txtQuantidade.setText(null);

            } else {

                item = tbProdVendas.getRowCount()+1 ;  

                

                venda.addRow(new Object[]{
                    item,
                    p.getCodigo(),
                    p.getDescricao(),
                    p.getUnidade(),
                    qtdProd,
                    p.getPrecoFormatado(),
                    valorTotal

                });

                totalVenda += valorTotal;
                txtSubTotal.setText(String.valueOf(dinheiro.format(totalVenda)));

                txtCodProd.setText(null);
                txtQuantidade.setText(null);
                calcularDesconto();

            }
        }
    }//GEN-LAST:event_txtCodProdActionPerformed

