    private void calcularDesconto() {

        try {
            if (txtDesconto.getText().equals("")) {
                desconto = 0f;

            } else {

                desconto = Double.valueOf(txtDesconto.getText().replaceAll(",", "."));
            }

            totalFinal = totalVenda - desconto;
            txtTotalVenda.setText(String.valueOf(dinheiro.format(totalFinal)));

        } catch (Exception e) {

            JOptionPane.showMessageDialog(null, "Desconto Invalido ");
            txtDesconto.setText("");

        }
    }

    private void preencerCliente() {

        ClienteDAO clienteDao = new ClienteDAO();
        Cliente objc = new Cliente();

        objc.setCgc(txtClienteVenda.getText());

        clienteDao.read(objc);

        txtNomeClienteVenda.setText(objc.getNome());

    }
    
    private void atualizarItem(){
        
        DefaultTableModel tbVenda = (DefaultTableModel) tbProdVendas.getModel();
        int it;
        
        for (it = 0; it < tbProdVendas.getRowCount(); it++) {
          
            tbVenda.setValueAt(it + 1, it, 0);
            
            
        }
        
    }
    
    private void excluirItem(){
        double ValorExcluido;

        if (tbProdVendas.getSelectedRow() != -1) {

            DefaultTableModel tbVenda = (DefaultTableModel) tbProdVendas.getModel();

            ValorExcluido = Double.parseDouble(tbProdVendas.getValueAt(tbProdVendas.getSelectedRow(), 6).toString());

            totalVenda -= ValorExcluido;

            tbVenda.removeRow(tbProdVendas.getSelectedRow());

            txtSubTotal.setText(String.valueOf(dinheiro.format(totalVenda)));

            calcularDesconto();
            atualizarItem();

        } else {
            JOptionPane.showMessageDialog(null, "Selecione um item para exclução!");
        }
        
    }

