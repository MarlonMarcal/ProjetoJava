    public void calcularIpi() {

        float pCompra;
        float percentIpi;
        float valorIpi;

        if (txtPrecoCompra.getText().isEmpty()) {

            pCompra = 0;

        } else {
            pCompra = Float.parseFloat(txtPrecoCompra.getText().replaceAll(",", "."));
        }
        if (txtPcIpi.getText().isEmpty()) {

            percentIpi = 0;

        } else {
            percentIpi = Float.parseFloat(txtPcIpi.getText().replaceAll(",", "."));

        }

        valorIpi = ((percentIpi / 100) * pCompra);

        txtIpi.setText(Float.toString(valorIpi).format("%.2f", valorIpi));

    }

