    private void jcMarcaAncestorAdded(javax.swing.event.AncestorEvent evt) {//GEN-FIRST:event_jcMarcaAncestorAdded
        // TODO add your handling code here:
        MarcaDAO dao = new MarcaDAO();
        List<Marca> lista = dao.readmarcas();

        jcMarca.removeAll();

        for (Marca m : lista) {

            jcMarca.addItem(m.getMarca());
        }
        jcMarca.setSelectedItem("");
    }//GEN-LAST:event_jcMarcaAncestorAdded

