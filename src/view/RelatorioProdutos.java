    private void btnGerarRelatorioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGerarRelatorioActionPerformed
        // TODO add your handling code here:
        try {

            String grupo;
            String marca;

            Date dataInicio = new SimpleDateFormat("dd/MM/yyyy").parse(dcDataInicio.getText());
            Date dataFim = new SimpleDateFormat("dd/MM/yyyy").parse(dcDataFim.getText());

            if (jcGrupo.getSelectedItem().equals(" ")) {
                grupo = "%";

            } else {
                grupo = jcGrupo.getSelectedItem().toString();
            }
            if (jcMarca.getSelectedItem().equals(" ")) {
                marca = "%";

            } else {
                marca = jcMarca.getSelectedItem().toString();
            }

            GerarRelatorio.gerarRelatorioProd(dataInicio, dataFim, grupo, marca);

            System.out.println("TESTE = data inicio: " + dcDataInicio.getText() + "   data fim: " + dcDataFim.getText());

        } catch (Exception e) {

            JOptionPane.showMessageDialog(null, e);

        }


    }//GEN-LAST:event_btnGerarRelatorioActionPerformed

