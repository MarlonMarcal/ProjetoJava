    public void btnsalvar() {

        FornecedorDAO dao = new FornecedorDAO();

        Fornecedor f = new Fornecedor();

        f.setCgc(txtCgc.getText());
        f.setNome(txtNome.getText());
        f.setCelular(ftxtCelular.getText());
        f.setTelefone(txtTelefone.getText());
        f.setEmail(txtEmail.getText());
        f.setCep(ftxtCep.getText());
        f.setEndereco(txtEndereco.getText());
        f.setComplemento(txtComplemento.getText());
        f.setNumero(txtNumero.getText());
        f.setBairro(txtBairro.getText());
        f.setCidade(txtCidade.getText());
        f.setUf(cbUF.getSelectedItem().toString());
        f.setInativo(cbxInativo.isSelected());

        //objcliente.setDatacadastro(txtDataCadastro.getText()));
        //objcliente.setDataalteracao(txtDataAlteracao.getText());
        //java.time.LocalDateTime;
        dao.salvar(f);
        dao.recuperarFornecedor(f);

        txtId.setText(String.valueOf(f.getIdFornecedor()));

        //txtDataCadastro.setText(sdf.format(objcliente.getDatacadastro()));
    }

