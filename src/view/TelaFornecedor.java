    public void comcep() {

        ViaCEP cep = new ViaCEP();

        try {

            cep.buscar(ftxtCep.getText());

            txtEndereco.setText(cep.getLogradouro());
            txtComplemento.setText(cep.getComplemento());
            txtBairro.setText(cep.getBairro());
            txtCidade.setText(cep.getLocalidade());
            cbUF.setSelectedItem(cep.getUf());

        } catch (ViaCEPException ex) {
            //Logger.getLogger(TelaCliente.class.getName()).log(Level.SEVERE, null, ex);

            JOptionPane.showMessageDialog(null, "ERRO ao buscar CEP " + ex);
        }

    }

