package model.bean;

/**
 *
 * @author Marlon
 */
public class Marca {

    private int codigo;
    private String marca;

    public int getCodigo() {
        return codigo;
    }

    public void setCodigo(int codigo) {
        this.codigo = codigo;
    }

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

    @Override
    public String toString() {
        return this.getMarca();
    }

}
